pub fn large_sum_first_ten() -> String {
    let mut sum = vec![0; 100]; // enough for carry
    for num_str in NUMBERS {
        let digits: Vec<u32> = num_str.chars().rev().map(|c| c.to_digit(10).unwrap()).collect();
        let mut carry = 0;
        for i in 0..digits.len() {
            let total = sum[i] + digits[i] + carry;
            sum[i] = total % 10;
            carry = total / 10;
        }
        let mut j = digits.len();
        while carry > 0 {
            let total = sum[j] + carry;
            sum[j] = total % 10;
            carry = total / 10;
            j += 1;
        }
    }
    sum.iter().rev().skip_while(|&&d| d == 0).take(10).map(|&d| (d as u8 + b'0') as char).collect()
}

